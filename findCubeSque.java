package java_class;

import java.util.Scanner;

public class findCubeSque {

	public static void main(String[] args) {
		Scanner input=new Scanner(System.in);
		float n=input.nextFloat();
		System.out.print("square: "+(n*n));
		System.out.println();
		System.out.print("cube: "+(n*n*n));
	}

}
